package com.example.MiniProject.controller.areaTable;

public class AreaDTO {
    private Long id;
    private float ropani;
    private float aana;
    private float paisa;
    private float daam;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public float getRopani() {
        return ropani;
    }

    public void setRopani(float ropani) {
        this.ropani = ropani;
    }

    public float getAana() {
        return aana;
    }

    public void setAana(float aana) {
        this.aana = aana;
    }

    public float getPaisa() {
        return paisa;
    }

    public void setPaisa(float paisa) {
        this.paisa = paisa;
    }

    public float getDaam() {
        return daam;
    }

    public void setDaam(float daam) {
        this.daam = daam;
    }
}
